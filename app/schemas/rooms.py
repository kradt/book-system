from pydantic import BaseModel


class Place(BaseModel):
    """
        :param column: number of column in columns
        :param row: number of row in rows
        :booked: if place booked set to True
        :additional_data: You can use it to pass some additional info like price of something else
        
    """
    column: int
    row: int
    booked: bool = False
    additional_data: dict | None = None
    

class Room(BaseModel):
    """
        :param name: Name of room if it has
        :param columns: count of columns in the room
        :param rows: count of rows in the room
        :param places: all places that generated by number of column and rows
    """
    name: str | None = None
    columns: int = 10
    rows: int = 5
    places: list[Place]
